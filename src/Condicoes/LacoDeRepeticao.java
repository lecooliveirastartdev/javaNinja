package Condicoes;

public class LacoDeRepeticao {

    public static void main(String[] args) {

        /*
        Lacos de Repetiçã:  repetir infinitamente ou ate você definir um parametro.
        WHILE = FOR

        while (condição) {tudo aqui vai acontecer}
         */
        int numeroDeClone = 0;
        int numeroMaximoDeClone = 40;

        while (numeroDeClone <= numeroMaximoDeClone) {
            numeroDeClone++;
            System.out.println("O naruto fez um clone das sombras " + numeroDeClone);


        }







    }
}
